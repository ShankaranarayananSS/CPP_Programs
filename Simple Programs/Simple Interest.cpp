//Simple Interest	: (Principal Amount * No. of Years * Rate of Interest) / 100

#include<iostream>
using namespace std;
int main(){
	float p,n,r;
	cout<<"Enter Principal amount:";
	cin>>p;
	cout<<"Enter No. of Years:";
	cin>>n;
	cout<<"Enter Principal amount:";
	cin>>r;
	cout<<"Simple Interest: "<<p*n*r/100;
}
