#include<iostream>
using namespace std;
int main(){
  float n1,n2;
  cout<<"Enter number-1:";
  cin>>n1;
  cout<<"Enter number-2:";
  cin>>n2;
  
  cout<<endl<<n1<<" + "<<n2<<" = "<<(n1+n2);
  cout<<endl<<n1<<" - "<<n2<<" = "<<(n1-n2);
  cout<<endl<<n1<<" * "<<n2<<" = "<<(n1*n2);
  cout<<endl<<n1<<" / "<<n2<<" = "<<(n1/n2);
  return 0;
}
